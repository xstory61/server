var i;
var check;
function start() 
{
	check = 0;
	id = cm.getMapId();
	for (i = 0; i < maps.length; i++)
	{
		if (id == maps[i][0])
		{
			cm.addJQPoints(maps[i][1]);
			cm.warp(910000000);
			cm.getPlayer().message("Received " + maps[i][1] + " Jump Quest Points");
			cm.dispose();
			check = 1;
			break;
		}
		if (check == 1) break;
	}
	if (check == 0){
	cm.sendOk("Hello There!");
	cm.dispose();
	}
}
